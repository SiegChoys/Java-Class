package ch3;

public class Op3 {
	public static void main(String[] args) {
	int a = 10, b = 20;
		
		System.out.println( a == b ); //F
		System.out.println( a != b ); //T
		System.out.println( a > b ); //F
		System.out.println( a >= b ); //F
		System.out.println( a < b ); //T
		System.out.println( a <= b ); //T
		
	}
}